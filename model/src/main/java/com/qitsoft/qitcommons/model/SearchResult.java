package com.qitsoft.qitcommons.model;

import java.util.List;

public class SearchResult<T> {

    private int total;

    private List<T> list;

    public List<T> getList() {
        return list;
    }

    public void setList(List<T> list) {
        this.list = list;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
